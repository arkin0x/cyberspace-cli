# Cantor Tree Proof (Multiple Pairs)
# =============================================================================

@dataclass
class CantorTreeProof:
    """ZK proof for a full Cantor tree over N leaves."""
    
    # Public inputs
    leaves: List[int]  # [temporal_seed, B_from, B_from+1, ..., B_to]
    root: int  # Claimed tree root
    
    # Proofs for each pairing operation
    pair_proofs: List[CantorProof]
    
    # Tree structure metadata
    tree_height: int
    constraints_satisfied: bool
    
    def to_dict(self) -> dict:
        """Serialize to dictionary."""
        return {
            'leaves': self.leaves,
            'root': self.root,
            'pair_proofs': [p.to_dict() for p in self.pair_proofs],
            'tree_height': self.tree_height,
            'constraints_satisfied': self.constraints_satisfied,
        }
    
    @classmethod
    def from_dict(cls, data: dict) -> 'CantorTreeProof':
        """Deserialize from dictionary."""
        return cls(
            leaves=data['leaves'],
            root=data['root'],
            pair_proofs=[CantorProof.from_dict(p) for p in data['pair_proofs']],
            tree_height=data['tree_height'],
            constraints_satisfied=data['constraints_satisfied'],
        )


def prove_cantor_tree(leaves: List[int]) -> CantorTreeProof:
    """
    Generate ZK proof for a full Cantor tree over N leaves.
    
    This proves the prover correctly built the entire tree, not just
    a single pairing.
    
    Args:
        leaves: List of leaf values (includes temporal seed + path)
    
    Returns:
        CantorTreeProof with proofs for all pairing operations
    """
    if len(leaves) < 2:
        return CantorTreeProof(
            leaves=leaves,
            root=leaves[0] if leaves else 0,
            pair_proofs=[],
            tree_height=0,
            constraints_satisfied=True,
        )
    
    # Build tree level by level, generating proofs
    current_level = leaves
    all_pair_proofs = []
    
    level_num = 0
    while len(current_level) > 1:
        next_level = []
        
        for i in range(0, len(current_level) - 1, 2):
            left = current_level[i]
            right = current_level[i + 1]
            
            # Generate proof for this pairing
            pair_proof = prove_cantor_pair(left, right)
            all_pair_proofs.append(pair_proof)
            
            # Use the result as input to next level
            next_level.append(pair_proof.z)
        
        # Handle odd number of elements (carry forward)
        if len(current_level) % 2 == 1:
            next_level.append(current_level[-1])
        
        current_level = next_level
        level_num += 1
    
    root = current_level[0]
    
    # Verify all pair proofs are valid
    all_valid = all(p.constraints_satisfied for p in all_pair_proofs)
    
    return CantorTreeProof(
        leaves=leaves,
        root=root,
        pair_proofs=all_pair_proofs,
        tree_height=level_num,
        constraints_satisfied=all_valid,
    )


def verify_cantor_tree(proof: CantorTreeProof) -> Tuple[bool, int]:
    """
    Verify a full Cantor tree proof.
    
    Verification is O(N) where N is number of leaves, but each
    verification step is O(1) field operations - much faster than
    recomputing the full Cantor tree.
    
    Args:
        proof: CantorTreeProof from prover
    
    Returns:
        Tuple of (is_valid, root_value)
    """
    leaves = proof.leaves
    root_claimed = proof.root
    pair_proofs = proof.pair_proofs
    
    # Rebuild tree from leaves, verifying each step
    current_level = leaves
    proof_index = 0
    
    while len(current_level) > 1:
        next_level = []
        
        for i in range(0, len(current_level) - 1, 2):
            left = current_level[i]
            right = current_level[i + 1]
            
            # Get the pair proof
            if proof_index >= len(pair_proofs):
                return False, 0
            
            pair_proof = pair_proofs[proof_index]
            proof_index += 1
            
            # Verify this pair proof
            is_valid, z = verify_cantor_pair(pair_proof)
            if not is_valid:
                return False, 0
            
            # Verify inputs match
            if pair_proof.x != left or pair_proof.y != right:
                return False, 0
            
            next_level.append(z)
        
        # Handle odd element
        if len(current_level) % 2 == 1:
            next_level.append(current_level[-1])
        
        current_level = next_level
    
    # Check root matches
    root_computed = current_level[0]
    if root_computed != root_claimed:
        return False, 0
    
    return True, root_claimed


