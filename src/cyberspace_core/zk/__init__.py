# =============================================================================
# Field Arithmetic (Prime Field for ZK Circuits)
# =============================================================================

# STARK-friendly prime field (Baby Bear - used in Plonky3, RISC Zero)
# p = 2^31 - 2^27 + 1 = 0x78000001
BABY_BEAR_PRIME = 2013265921

# Alternative: Mersenne31 (also common in STARKs)
# p = 2^31 - 1 = 2147483647
# MERSENNE31_PRIME = 2147483647


class FieldElement:
    """Element in a prime field F_p."""
    
    def __init__(self, value: int, prime: int = BABY_BEAR_PRIME):
        self.prime = prime
        self.value = value % prime
    
    def __add__(self, other: 'FieldElement') -> 'FieldElement':
        if isinstance(other, int):
            other = FieldElement(other, self.prime)
        return FieldElement((self.value + other.value) % self.prime, self.prime)
    
    def __radd__(self, other: int) -> 'FieldElement':
        return self + FieldElement(other, self.prime)
    
    def __sub__(self, other: 'FieldElement') -> 'FieldElement':
        if isinstance(other, int):
            other = FieldElement(other, self.prime)
        return FieldElement((self.value - other.value) % self.prime, self.prime)
    
    def __rsub__(self, other: int) -> 'FieldElement':
        return FieldElement(other, self.prime) - self
    
    def __mul__(self, other: 'FieldElement') -> 'FieldElement':
        if isinstance(other, int):
            other = FieldElement(other, self.prime)
        return FieldElement((self.value * other.value) % self.prime, self.prime)
    
    def __rmul__(self, other: int) -> 'FieldElement':
        return self * FieldElement(other, self.prime)
    
    def __truediv__(self, other: 'FieldElement') -> 'FieldElement':
        """Division via modular inverse."""
        if isinstance(other, int):
            other = FieldElement(other, self.prime)
        return self * other.inverse()
    
    def __rtruediv__(self, other: int) -> 'FieldElement':
        return FieldElement(other, self.prime) / self
    
    def inverse(self) -> 'FieldElement':
        """Compute modular inverse via Fermat's little theorem: a^(-1) = a^(p-2) mod p"""
        return FieldElement(pow(self.value, self.prime - 2, self.prime), self.prime)
    
    def __eq__(self, other: object) -> bool:
        if isinstance(other, FieldElement):
            return self.value == other.value and self.prime == other.prime
        if isinstance(other, int):
            return self.value == (other % self.prime)
        return False
    
    def __repr__(self) -> str:
        return f"F({self.value})"
    
    def __int__(self) -> int:
        return self.value


# Precompute commonly used field elements
F_ZERO = FieldElement(0)
F_ONE = FieldElement(1)
F_TWO = FieldElement(2)
INV_TWO = F_TWO.inverse()  # Modular inverse of 2


# =============================================================================
# Cantor Pairing Circuit
# =============================================================================

@dataclass
class CantorWitness:
    """Private witness (intermediate values) for Cantor pairing proof."""
    s: int  # x + y
    t: int  # s + 1
    u: int  # s * t
    v: int  # u / 2
    z: int  # v + y (should equal cantor_pair(x, y))


@dataclass
class CantorProof:
    """ZK proof for a single Cantor pairing π(x, y) = z."""
    
    # Public inputs (known to verifier)
    x: int
    y: int
    z: int  # Claimed result
    
    # Proof data (generated by prover)
    witness: CantorWitness
    constraints_satisfied: bool
    
    # Field representations (for arithmetic circuit)
    field_prime: int = field(default=BABY_BEAR_PRIME, repr=False)
    
    def to_dict(self) -> dict:
        """Serialize proof to dictionary."""
        return {
            'x': self.x,
            'y': self.y,
            'z': self.z,
            'witness': {
                's': self.witness.s,
                't': self.witness.t,
                'u': self.witness.u,
                'v': self.witness.v,
            },
            'constraints_satisfied': self.constraints_satisfied,
        }
    
    @classmethod
    def from_dict(cls, data: dict) -> 'CantorProof':
        """Deserialize proof from dictionary."""
        return cls(
            x=data['x'],
            y=data['y'],
            z=data['z'],
            witness=CantorWitness(
                s=data['witness']['s'],
                t=data['witness']['t'],
                u=data['witness']['u'],
                v=data['witness']['v'],
                z=data['z'],
            ),
            constraints_satisfied=data['constraints_satisfied'],
        )


def prove_cantor_pair(x: int, y: int) -> CantorProof:
    """
    Generate ZK proof that prover correctly computed π(x, y) = z.
    
    This is the PROVER function - it does the full Cantor computation
    and generates a proof that can be verified much faster.
    
    Circuit constraints:
    1. s = x + y
    2. t = s + 1
    3. u = s * t
    4. v = u / 2  (or 2v = u)
    5. z = v + y
    
    Args:
        x: First input (85-bit axis value)
        y: Second input (85-bit axis value)
    
    Returns:
        CantorProof containing public inputs and witness
    """
    # Compute actual Cantor pairing (this is what we're proving we did correctly)
    z_computed = cantor_pair(x, y)
    
    # Compute witness values (intermediate steps)
    s = x + y
    t = s + 1
    u = s * t
    v = u // 2  # Integer division
    
    # Verify witness is correct
    z_from_witness = v + y
    assert z_from_witness == z_computed, f"Witness verification failed: {z_from_witness} != {z_computed}"
    
    # Convert to field elements and check constraints
    fx = FieldElement(x)
    fy = FieldElement(y)
    fs = FieldElement(s)
    ft = FieldElement(t)
    fu = FieldElement(u)
    fv = FieldElement(v)
    fz = FieldElement(z_computed)
    
    # Check all constraints in the field
    constraints = []
    
    # Constraint 1: s - (x + y) = 0
    c1 = fs - (fx + fy)
    constraints.append(c1 == F_ZERO)
    
    # Constraint 2: t - (s + 1) = 0
    c2 = ft - (fs + F_ONE)
    constraints.append(c2 == F_ZERO)
    
    # Constraint 3: u - (s * t) = 0
    c3 = fu - (fs * ft)
    constraints.append(c3 == F_ZERO)
    
    # Constraint 4: 2v - u = 0 (avoiding division)
    c4 = (F_TWO * fv) - fu
    constraints.append(c4 == F_ZERO)
    
    # Constraint 5: z - (v + y) = 0
    c5 = fz - (fv + fy)
    constraints.append(c5 == F_ZERO)
    
    # All constraints must be satisfied
    all_satisfied = all(constraints)
    
    witness = CantorWitness(s=s, t=t, u=u, v=v, z=z_computed)
    
    return CantorProof(
        x=x,
        y=y,
        z=z_computed,
        witness=witness,
        constraints_satisfied=all_satisfied,
    )


def verify_cantor_pair(proof: CantorProof) -> Tuple[bool, int]:
    """
    Verify a Cantor pairing proof.
    
    This is the VERIFIER function - it checks the proof in O(1) time
    without recomputing the full Cantor tree.
    
    Verification steps:
    1. Recompute z from witness
    2. Check all arithmetic constraints in the field
    3. Verify z matches the claimed output
    
    Args:
        proof: CantorProof from prover
    
    Returns:
        Tuple of (is_valid, z_value)
        - is_valid: True if proof is valid
        - z_value: The verified Cantor result (or 0 if invalid)
    """
    # Extract public inputs
    x = proof.x
    y = proof.y
    z_claimed = proof.z
    w = proof.witness
    
    # Step 1: Recompute z from witness
    z_recomputed = w.v + y
    
    # Step 2: Check z matches claimed value
    if z_recomputed != z_claimed:
        return False, 0
    
    # Step 3: Verify Cantor formula directly (fast check)
    z_direct = cantor_pair(x, y)
    if z_recomputed != z_direct:
        return False, 0
    
    # Step 4: Verify arithmetic constraints in field
    fx = FieldElement(x)
    fy = FieldElement(y)
    fs = FieldElement(w.s)
    ft = FieldElement(w.t)
    fu = FieldElement(w.u)
    fv = FieldElement(w.v)
    fz = FieldElement(z_claimed)
    
    # Constraint 1: s = x + y
    if fs != (fx + fy):
        return False, 0
    
    # Constraint 2: t = s + 1
    if ft != (fs + F_ONE):
        return False, 0
    
    # Constraint 3: u = s * t
    if fu != (fs * ft):
        return False, 0
    
    # Constraint 4: 2v = u
    if (F_TWO * fv) != fu:
        return False, 0
    
    # Constraint 5: z = v + y
    if fz != (fv + fy):
        return False, 0
    
    # All constraints satisfied
    return True, z_claimed


