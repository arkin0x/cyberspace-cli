def proof_size_bytes(proof: CantorProof) -> int:
    """Estimate serialized proof size in bytes."""
    import json
    proof_dict = proof.to_dict()
    json_str = json.dumps(proof_dict)
    return len(json_str.encode('utf-8'))


def tree_proof_size_bytes(proof: CantorTreeProof) -> int:
    """Estimate serialized tree proof size in bytes."""
    import json
    proof_dict = proof.to_dict()
    json_str = json.dumps(proof_dict)
    return len(json_str.encode('utf-8'))


def benchmark_cantor_pair(x: int = 12345, y: int = 67890) -> dict:
    """
    Benchmark proof generation and verification for a single Cantor pair.
    
    Returns:
        Dictionary with timing and size metrics
    """
    import time
    
    # Prove
    start = time.perf_counter()
    proof = prove_cantor_pair(x, y)
    prove_time = time.perf_counter() - start
    
    # Verify
    start = time.perf_counter()
    is_valid, z = verify_cantor_pair(proof)
    verify_time = time.perf_counter() - start
    
    # Size
    size_bytes = proof_size_bytes(proof)
    
    return {
        'x': x,
        'y': y,
        'z': z,
        'is_valid': is_valid,
        'prove_time_sec': prove_time,
        'verify_time_sec': verify_time,
        'prove_time_ms': prove_time * 1000,
        'verify_time_ms': verify_time * 1000,
        'proof_size_bytes': size_bytes,
        'speedup': prove_time / verify_time if verify_time > 0 else float('inf'),
    }


def benchmark_cantor_tree(num_leaves: int = 8) -> dict:
    """
    Benchmark proof generation and verification for a Cantor tree.
    
    Args:
        num_leaves: Number of leaves in the tree (power of 2 for simplicity)
    
    Returns:
        Dictionary with timing and size metrics
    """
    import time
    
    # Generate random leaves
    leaves = list(range(1000, 1000 + num_leaves))
    
    # Prove
    start = time.perf_counter()
    proof = prove_cantor_tree(leaves)
    prove_time = time.perf_counter() - start
    
    # Verify
    start = time.perf_counter()
    is_valid, root = verify_cantor_tree(proof)
    verify_time = time.perf_counter() - start
    
    # Size
    size_kb = tree_proof_size_bytes(proof) / 1024
    
    # Count pairings
    num_pairings = len(proof.pair_proofs)
    
    return {
        'num_leaves': num_leaves,
        'tree_height': proof.tree_height,
        'num_pairings': num_pairings,
        'root': root,
        'is_valid': is_valid,
        'prove_time_sec': prove_time,
        'verify_time_sec': verify_time,
        'prove_time_ms': prove_time * 1000,
        'verify_time_ms': verify_time * 1000,
        'proof_size_kb': size_kb,
        'avg_pair_prove_ms': (prove_time * 1000) / num_pairings if num_pairings > 0 else 0,
        'avg_pair_verify_ms': (verify_time * 1000) / num_pairings if num_pairings > 0 else 0,
        'speedup': prove_time / verify_time if verify_time > 0 else float('inf'),
    }


